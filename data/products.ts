const productSeeds: ProductSeed[] = [
  ...referencePackagingProducts,
  ...legacyProductSeeds,
];

// Replace this seed data with a database or CMS fetch when product management is connected.
export const products: Product[] = productSeeds.map((product) => ({
  ...product,
  id: product.slug,
  imageKey: product.sourceImages?.[0] || `products/${product.slug}/main.webp`,
  images:
    product.sourceImages && product.sourceImages.length > 0
      ? product.sourceImages
      : referenceCategoryImagesByName[product.category] || [
          buildPublicAssetUrl(`products/${product.slug}/main.webp`),
          buildPublicAssetUrl(`products/${product.slug}/detail.webp`),
        ],
}));

export function getProductBySlug(slug: string) {
  return products.find((product) => product.slug === slug);
}

export function getRelatedProducts(product: Product) {
  return products
    .filter(
      (candidate) =>
        candidate.slug !== product.slug &&
        (candidate.category === product.category ||
          candidate.material === product.material),
    )
    .slice(0, 4);
}

export const featuredProducts = products.filter((product) => product.featured).slice(0, 8);
export const popularProducts = products.filter((product) => product.popular).slice(0, 8);
